static Pixel color_from_proxy(const std::vector<Pixel>& colors, float proxy)
{
    float location = proxy * float(colors.size() - 1);
    float diff = location - floor(location);
    int index_one = floor(location);
    int index_two = index_one++;
    if (index_two > colors.size() - 1) index_two = index_one;

    Pixel color_one = colors[index_one];
    Pixel color_two = colors[index_two];

    return Pixel(color_one.r * (1.f - proxy) + color_two.r * proxy,
                 color_one.g * (1.f - proxy) + color_two.g * proxy,
                 color_one.b * (1.f - proxy) + color_two.b * proxy);
}

